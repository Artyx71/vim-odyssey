export const getChallengeById = (worldId, challengeId) => {
    const world = getWorldById(worldId);
    return world?.challenges.find(c => c.id === challengeId);
};
export const getTotalStars = () => ALL_WORLDS.reduce((sum, w) => sum + w.challenges.length * 3, 0);
